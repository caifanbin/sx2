package com.binge.sx2.beans;


/**
 * 销售地区
 */
public class Region {


    /**
     * 地区id
     */
    private Integer id;

    /**
     * 地区名称
     */
    private String regionname;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getRegionname() {
        return regionname;
    }

    public void setRegionname(String regionname) {
        this.regionname = regionname;
    }
}
